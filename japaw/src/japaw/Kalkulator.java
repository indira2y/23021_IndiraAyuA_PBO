    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {                                          
        double secondNum = Double.parseDouble(jTextField1.getText());
        double result = 0;
    switch (operation) {
        case "+":
            result = firstNum + secondNum;
            break;
        case "-":
            result = firstNum - secondNum;
            break;
        case "*":
            result = firstNum * secondNum;
            break;
        case "/":
            result = firstNum / secondNum;
            break;
        }
    jTextField1.setText(String.valueOf(result));
    }                                         

